print("42:    ",type(42))
print("3.14:  ",type(3.14))
print("python:",type("python"))
print("True:  ",type(True))
print('"42":  ',type("42"))
print("0:     ",type(0))
print('" ":   ',type(" "))
#"42" looks like a number but Python sees it as text (str). Quotes make it a string.